# -*- coding: utf-8 -*-
"""EasyOCR 한국어 인식 결과 후처리 — 반복 오인식 패턴 보정."""
import re

# ──────────────────────────────────────────────────────────────────────────────
# 알려진 오인식 패턴 → 정답 매핑 테이블
#
# EasyOCR이 한국어 문서를 인식할 때 자주 틀리는 단어/구를 미리 목록으로 정의해 둡니다.
# 각 항목은 (틀린 인식 결과, 올바른 텍스트) 형태의 튜플입니다.
#
# 주의: 긴 구(phrase) 패턴을 앞에 배치하는 이유는, 짧은 단어를 먼저 치환하면
#       이후에 긴 구를 찾지 못하는 "부분 치환 충돌"이 발생할 수 있기 때문입니다.
#       예를 들어 "전락"을 먼저 고치면 "전략 기술"이라는 구를 더 이상 찾을 수 없습니다.
# ──────────────────────────────────────────────────────────────────────────────
_WORD_FIXES: list[tuple[str, str]] = [
    # ── 구(phrase) 단위 ──────────────────────────────
    # 두 단어 이상으로 이루어진 패턴을 먼저 처리합니다.
    ("끈 노이만",   "폰 노이만"),   # Von Neumann — "폰"의 초성 'ㅍ'이 'ㄲ'으로 오인식된 경우
    ("전락 기술",   "전략 기술"),   # "전락"(downfall)과 혼동되지 않도록 구 단위로만 교정

    # ── 외래어·기술 용어 ─────────────────────────────
    # IT·의료 분야에서 자주 등장하는 영어 외래어의 오인식 패턴입니다.
    ("세그만트",    "세그먼트"),    # segment — '먼' 모음 'ㅓ'가 'ㅏ'로 오인식
    ("헬스레어",    "헬스케어"),    # healthcare — 'ㅋ'이 'ㄹ'으로 오인식
    ("헬스웨어",    "헬스케어"),    # healthcare — 'ㅋ'이 'ㅇ'으로 오인식
    ("스마트포",    "스마트폰"),    # smartphone — 종성 'ㄴ'이 누락된 경우
    ("옛지",        "엣지"),        # edge — 'ㅅ'이 분리 인식되어 '옛'으로 오인식
    ("가드너",      "가트너"),      # Gartner — 'ㅌ'이 'ㄷ'으로 오인식

    # ── 한국어 어휘 ─────────────────────────────────
    # 순수 한국어 단어에서 자주 발생하는 오인식 패턴입니다.
    ("용용적",      "응용적"),      # 응용(application) — 초성 'ㅇ'가 두 번 반복되는 오인식
    ("학신적",      "혁신적"),      # 혁신(innovation) — '혁'의 모음 'ㅕ'가 'ㅏ'로 오인식
    ("학신올",      "혁신을"),      # '혁신을'에서 조사 '을'이 '올'로 오인식된 형태
    ("확기적",      "획기적"),      # 획기(epoch-making) — '획'의 모음 오인식
    ("파악하엿",    "파악하였"),    # 과거형 어미 '였'이 '엿'으로 오인식 (활용형 전체 커버)
    ("활용월",      "활용될"),      # '될'의 종성이 월(month)처럼 오인식된 경우
    ("소비률",      "소비를"),      # 조사 '를'이 '률(rate)'처럼 오인식된 경우
]

# ──────────────────────────────────────────────────────────────────────────────
# 조사 오인식 교정을 위한 정규식 패턴
#
# 한글 받침(종성)이 있는 음절 바로 뒤에 오는 '올' 또는 '름'을 찾습니다.
# 이 패턴들은 EasyOCR이 '을'→'올', '를'→'름'으로 잘못 인식하는 경우입니다.
#
# 정규식 설명:
#   [가-힣]   : 모든 한글 음절 한 글자 (첫 번째 캡처 그룹)
#   (올|름)   : '올' 또는 '름' 중 하나 (두 번째 캡처 그룹)
#   (?=...)   : lookahead — 공백, 구두점, 줄끝이 뒤따를 때만 매칭
#               (단어 중간에 있는 '올'이나 '름'은 조사가 아니므로 제외)
# ──────────────────────────────────────────────────────────────────────────────
_JOSA_RE = re.compile(r'([가-힣])(올|름)(?=[\s\.,;:!?\n\r]|$)')


def _josa_fix(m: re.Match) -> str:
    """정규식 매칭 결과를 받아 조사 '올'→'을', '름'→'를'로 교정하는 내부 함수.

    매개변수:
        m (re.Match): 정규식 매칭 객체.
                      그룹1 = 앞 한글 음절, 그룹2 = 오인식된 조사('올' 또는 '름')

    반환값:
        str: 교정된 문자열. 받침이 없는 음절 뒤라면 원본 그대로 반환합니다.
    """
    char, josa = m.group(1), m.group(2)

    # 유니코드 한글 음절의 구조:
    #   코드포인트 = 0xAC00 + (초성 × 21 + 중성) × 28 + 종성
    # 종성(받침) 인덱스가 0이면 받침이 없는 글자입니다.
    has_jongseong = (ord(char) - 0xAC00) % 28 != 0

    if not has_jongseong:
        # 받침이 없는 글자 뒤에는 '올'/'름'이 조사가 아닐 가능성이 높으므로 변경하지 않습니다.
        return m.group(0)

    # 받침이 있는 글자 뒤: '올'→'을', '름'→'를' 로 교정합니다.
    return char + ("을" if josa == "올" else "를")


def fix_easyocr_korean(text: str) -> str:
    """EasyOCR 한국어 오인식 결과를 후처리하여 올바른 텍스트로 보정합니다.

    두 단계로 보정을 수행합니다:
      1단계: _WORD_FIXES 테이블을 순서대로 순회하며 알려진 오인식 단어/구를 사전 치환합니다.
      2단계: 정규식을 사용하여 받침 있는 음절 뒤 조사 '올'→'을', '름'→'를'을 교정합니다.

    매개변수:
        text (str): EasyOCR이 인식한 원본 한국어 텍스트.

    반환값:
        str: 오인식 패턴이 보정된 텍스트.
    """
    # 1단계: 사전에 정의된 단어/구 오인식 패턴을 순서대로 치환합니다.
    # _WORD_FIXES 리스트에서 (틀린표현, 올바른표현) 쌍을 하나씩 꺼내 replace를 수행합니다.
    for wrong, right in _WORD_FIXES:
        text = text.replace(wrong, right)

    # 2단계: 정규식으로 조사 오인식('올'→'을', '름'→'를')을 교정합니다.
    # _JOSA_RE.sub()는 패턴에 매칭되는 모든 부분을 _josa_fix 함수의 반환값으로 치환합니다.
    return _JOSA_RE.sub(_josa_fix, text)
